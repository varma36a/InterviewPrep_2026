def _q(id: str, question: str, stub: str = "", points: list[str] | None = None) -> InterviewItem:
    return InterviewItem(
        id=id,
        question=question,
        explanation="See detailed explanation.",
        code=stub,
        language="text",
        key_points=points or [],
    )


